int	ft_atoi(char *str)
{
	int	i;
	int	sinal;
	int	nbr;

	i = 0;
	sinal = 0;
	nbr = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
	{
		i++;
	}
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sinal++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nbr = nbr * 10 + (str[i] - '0');
		i++;
	}
	if (sinal % 2 == 1)
		nbr *= -1;
	return (nbr);
}
